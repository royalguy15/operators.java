package Operators;

public class arithmetic {
    public static void main(String args[]) {
        int A = 10;
        int B = 5;

        System.out.println("add:" + (A + B));
        System.out.println("sub:" + (A - B));
        System.out.println("mul:" + (A * B));
        System.out.println("div:" + (A / B));
        System.out.println("modulo:" + (A % B));
    }

}
